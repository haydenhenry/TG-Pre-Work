let cupsOfSugarNeeded = 100;
let cupsAdded = 0;

do {
  cupsAdded++
} while (cupsAdded < cupsOfSugarNeeded);


// In this lesson I learned how to use a do-while loop. This loop can perform a task while a certain condtion is met.
// In the example, the loop checks if the value of cupsAdded is less than cupsOfSugarNeeded, and every iteration of the loop where that
// criteria is met, it adds an iteration to cupsAdded. This will repeat until the condition is no longer met.
