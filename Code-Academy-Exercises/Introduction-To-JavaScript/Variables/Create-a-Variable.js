// Let lesson

let changeMe = true;
changeMe = false;
console.log(changeMe);

// the Let keyword similarly creates a variable but signals that the variable can be reassigned a different value.

// Const lesson

const entree = "Enchiladas";
console.log(entree);
entree = 'Tacos';

// The const keyword creates a variable that does not change value. It must be initialized with a value.
